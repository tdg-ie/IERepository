package data;

public class LiteralPair {
	private Literal literal;
	private Literal negatedLiteral;
	
	public Literal getLiteral() {
		return literal;
	}
	public void setLiteral(Literal literal) {
		this.literal = literal;
	}
	public Literal getNegatedLiteral() {
		return negatedLiteral;
	}
	public void setNegatedLiteral(Literal negatedLiteral) {
		this.negatedLiteral = negatedLiteral;
	}
}
